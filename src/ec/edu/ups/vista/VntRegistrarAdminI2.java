    public boolean telefono(String telefono) {
        if (cbxTipoTelf.getSelectedItem().equals("Casa")) {
            System.out.println("casa");
            Pattern tipo = Pattern.compile("^(4|2)?\\d{6,6}$");
            Matcher m = tipo.matcher(telefono);
            System.out.println("1 " + m.matches());
            return m.matches();
        } else if (cbxTipoTelf.getSelectedItem().equals("Movil")) {
            System.out.println("movil");
            Pattern tipo = Pattern.compile("^(09)?\\d{8,8}$");
            Matcher m = tipo.matcher(telefono);
            System.out.println("1 " + m.matches());
            return m.matches();
        }
        return false;
    }

    public boolean cedula(String numCedula) {
        Pattern p = Pattern.compile("^[0-2]\\d{9,9}$");
        Matcher m = p.matcher(numCedula);
        System.out.println("2 " + m.matches());
        return m.matches();
    }

    public boolean correoC(String correo) {
        Pattern texto = Pattern.compile("^\\S\\w{3,}@\\w{2,}(\\.\\w{2,})+$");
        Matcher corpus = texto.matcher(correo);
        System.out.println("3 " + corpus.matches());
        return corpus.matches();
    }

